function isCanonicalBattleTerm(value: string): boolean {
  const segments = value
    .split(/\s*(?:\/|·|,)\s*/)
    .map(segment => segment.trim())
    .filter(Boolean);

  return segments.length > 0 && segments.every(segment => canonicalBattleTerms.has(segment));
}

